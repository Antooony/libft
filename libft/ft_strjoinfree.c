#include "libft.h"

char	*ft_strjoinfree(char *s1, char *s2)
{
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	str = ft_strnew(ft_strlen(s1) + ft_strlen(s2));
	if (str == NULL)
		return (NULL);
	str = ft_strcat(ft_strcpy(str, s1), s2);
	ft_strdel(&s1);
	ft_strdel(&s2);
	return (str);
}
